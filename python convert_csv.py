def convert_csv_to_mql5_array(csv_data_string):
    """
    CSV形式の文字列データをMQL5のTradeSignal構造体配列の初期化コードに変換します。
    スペース区切りとカンマ区切りの両方に対応を試みます。
    """
    data_io = StringIO(csv_data_string)
    lines = data_io.readlines()
    
    mql5_array_elements = []
    
    pattern_space_separated = re.compile(r"(\d{4}-\d{2}-\d{2}\s\d{2}:\d{2}:\d{2})\s+(\d+)\s+(\d+)\s+(\d+)")

    for i, line in enumerate(lines):
        line_content = line.strip()
        
        if i == 0 and line_content.startswith("entry_time,signal,tp_pips,sl_pips"):
            continue
            
        if not line_content:
            continue
            
        entry_time_str, signal_str, tp_pips_str, sl_pips_str = None, None, None, None
        
        match = pattern_space_separated.match(line_content)
        if match:
            entry_time_str, signal_str, tp_pips_str, sl_pips_str = match.groups()
        else:
            parts = [p.strip() for p in line_content.split(',')]
            if len(parts) == 4:
                entry_time_str, signal_str, tp_pips_str, sl_pips_str = parts
            else:
                print(f"警告: {i+1}行目は処理できない形式のためスキップします: {line_content}")
                continue
        
        try:
            dt_obj = pd.to_datetime(entry_time_str, format='%Y-%m-%d %H:%M:%S')
            mql5_datetime = f"D'{dt_obj.strftime('%Y.%m.%d %H:%M:%S')}'"
        except ValueError:
            print(f"警告: {i+1}行目の日時形式 ('{entry_time_str}') が不正なためスキップします。")
            continue
        
        try:
            signal = int(signal_str)
            tp_pips = float(tp_pips_str)
            sl_pips = float(sl_pips_str)
        except ValueError:
            print(f"警告: {i+1}行目の数値形式 (s:'{signal_str}', tp:'{tp_pips_str}', sl:'{sl_pips_str}') が不正なためスキップします。")
            continue
            
        mql5_array_elements.append(f"  {{ {mql5_datetime}, {signal}, {tp_pips:.1f}, {sl_pips:.1f} }}")
        
    if not mql5_array_elements:
        return "// 変換対象のデータが見つからないか、全てのデータが不正でした。\n// TradeSignal g_trade_signals[] = {}; // 空の配列として定義"
        
    mql5_code = "TradeSignal g_trade_signals[] =\n{\n"
    mql5_code += ",\n".join(mql5_array_elements)
    mql5_code += "\n};"
    
    return mql5_code

